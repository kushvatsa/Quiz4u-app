     private Set<Integer> setQuestionList(){
        Set<Integer> qlist=new HashSet<Integer>();
        Random random=new Random();
        int size=qlist.size();
        while(size<30){
            int value=random.nextInt(90);
            if(!qlist.contains(value) && value!=0){
                qlist.add(value);
                size+=1;
            }
        }
        return qlist;
    }
