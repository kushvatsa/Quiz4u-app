     private String getDateNow(){
        Date d=new Date();
        SimpleDateFormat sf=new SimpleDateFormat("yyyy.dd.MM");
        String da=sf.format(d);
       return da.replace(".", "-");
    }
    private void loadSubjectlist() throws SQLException{
        jComboBox1.removeAllItems();
        ResultSet rs=stmt.executeQuery("select * from subjects");
        while(rs.next()){
            jComboBox1.addItem(rs.getString(2));
        }
    }
    private int getSubid(String subjectName) throws SQLException{
        ResultSet rs=stmt.executeQuery("select SubId from subjects where SubName='"+subjectName+"'");
        while(rs.next()){
            return rs.getInt(1);
        }
        return 0;
    }

